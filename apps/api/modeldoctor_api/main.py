@app.post("/api/v1/models/upload")
async def upload_model(file: UploadFile = File(...), intended_use: str = "general", db: Store = Depends(get_store)) -> InspectionRun:
    suffix = Path(file.filename or "").suffix.lower()
    if suffix not in {".stl", ".obj", ".glb", ".gltf"}:
        raise HTTPException(status_code=415, detail="Unsupported format. Export to STL, OBJ or GLB for this MVP.")
    try:
        model_id, path, checksum, duplicate = await db.save_upload(file)
    except ValueError as exc:
        raise HTTPException(status_code=413, detail=str(exc)) from exc
    return _create_run(db, model_id, path, checksum, duplicate, intended_use)


@app.post("/api/v1/models/{model_id}/files")
async def upload_model_file(model_id: str, file: UploadFile = File(...), intended_use: str = "general", db: Store = Depends(get_store)) -> InspectionRun:
    return await upload_model(file=file, intended_use=intended_use, db=db)


@app.post("/api/v1/models/{model_id}/inspection-runs")
def create_inspection_run(model_id: str, intended_use: str = "general", db: Store = Depends(get_store)) -> InspectionRun:
    path = db.files.get(model_id)
    if not path:
        raise HTTPException(status_code=404, detail="Model file not found.")
    checksum = path.read_bytes().hex()[:64]
    return _create_run(db, model_id, path, checksum, False, intended_use)


