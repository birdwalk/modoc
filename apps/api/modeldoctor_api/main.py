def _create_run(db: Store, model_id: str, path: Path, checksum: str, duplicate: bool, intended_use: str) -> InspectionRun:
    try:
        parsed = engine.parse(path)
    except ParseError as exc:
        raise HTTPException(status_code=422, detail=str(exc)) from exc
    mesh, parser, parser_version = parsed.mesh, parsed.parser, parsed.parser_version
    if len(mesh.faces) > settings.max_triangles:
        raise HTTPException(status_code=422, detail=f"This model has {len(mesh.faces):,} triangles, above the current MODOC analysis limit of {settings.max_triangles:,}. Reduce or decimate the mesh, or raise MODELDOCTOR_MAX_TRIANGLES for a larger workstation demo.")
    version_id = db.new_id("version")
    db.meshes[version_id] = mesh
    return _create_run_from_mesh(db, model_id, version_id, mesh, path.name, intended_use, [f"{_now()} Source checksum {checksum}.", f"{_now()} Duplicate upload detected: {duplicate}."] , parser, parser_version)


def _create_run_from_mesh(db: Store, model_id: str, version_id: str, mesh, filename: str, intended_use: str, audit: list[str], parser: str = "native", parser_version: str = "0.1") -> InspectionRun:
    start = time.perf_counter()
    metrics = compute_metrics(mesh, filename, parser, parser_version)
    issues = run_inspections(mesh, intended_use)
    scores = calculate_scores(issues, intended_use)
    run = InspectionRun(
        id=db.new_id("run"),
        model_id=model_id,
        version_id=version_id,
        intended_use=intended_use,
        status=ProcessingStage.COMPLETED,
        metrics=metrics,
        issues=issues,
        health_scores=scores,
        analysis_duration_ms=round((time.perf_counter() - start) * 1000),
        created_at=_now(),
        audit_trail=audit + [
            f"{_now()} Parsed geometry.",
            f"{_now()} Computed metrics.",
            f"{_now()} Ran deterministic inspections.",
            f"{_now()} Generated viewer payload.",
        ],
    )
    db.runs[run.id] = run
    return run


def _now() -> str:
    return datetime.now(timezone.utc).isoformat()
